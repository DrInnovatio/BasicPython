# Variable name rules

# Variable names can only contain letters, numbers and underscores. 
# Special characters such as !#$$^%@&@ are not allowed as well as spaces.

# Variable names cannot start with numbers.
# var2 is valid, but 2var is invalid.

# Variable names are case sensitive.
# age and Age are different.

# Reserved words cannot be used. 

# Variable names have to have meanings.


print("This program calculates the average of two numbers!!");

